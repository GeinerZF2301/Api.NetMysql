using APIMySQL.NET.Data.DatabaseConfig;
using APIMySQL.NET.Data.Repositories;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
//Database Configuration
var stringConnection = builder.Configuration.GetConnectionString("MysqlConnection");
var mysqlConfiguration = new MysqlConfiguration(stringConnection);
builder.Services.AddSingleton(mysqlConfiguration);


builder.Services.AddScoped<ICategoryRepository, CategoryRepository>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
